                    <p>
                    In 1993, Norodom Sihanouk was restored as King of Cambodia, but all power was in the hands of the government established after the UNTAC sponsored elections. The stability established following the conflict was shaken in 1997 by a coup d'état led by the co-Prime Minister Hun Sen against the noncommunist parties in the government.[66] In recent years, reconstruction efforts have progressed and led to some political stability through a multiparty democracy under a constitutional monarchy.[1]

In July 2010 Kang Kek Iew was the first Khmer Rouge member found guilty of war crimes and crimes against humanity in his role as the former commandant of the S21 extermination camp and he was sentenced to life in prison.[67][68] However, Hun Sen has opposed extensive trials of former Khmer Rouge mass murderers.[69]

In August 2014, a U.N.-backed war crimes tribunal, the Extraordinary Chambers in the Courts of Cambodia (also known as the Khmer Rouge Tribunal), sentenced Khieu Samphan, the regime's 83-year-old former head of state, and Nuon Chea, its 88-year-old chief ideologue to life in prison on war crimes charges for their role in the country's terror period in the 1970s. The trial began in November 2011. Former Foreign Minister Ieng Sary died in 2013, while his wife, Social Affairs Minister Ieng Thirith, was deemed unfit to stand trial due to dementia in 2012. The group's top leader, Pol Pot, died in 1998.
