                <h2 class="sub-title">Politics</h2>
				<h3>Government</h3>
            	<p>
                	Officially a multiparty democracy, in reality "the country remains a one-party state dominated by the Cambodian People's Party and Prime Minister Hun Sen, a recast Khmer Rouge official in power since 1985. The open doors to new investment during his reign have yielded the most access to a coterie of cronies of his and his wife, Bun Rany."[70] Cambodia's government has been described by Human Rights Watch's Southeast Asian Director, David Roberts, as a "vaguely communist free-market state with a relatively authoritarian coalition ruling over a superficial democracy."[17]

Prime Minister Hun Sen has vowed to rule until he is 74.[71][72] He is a former Khmer Rouge member who defected. His government is regularly accused of ignoring human rights and suppressing political dissent. The 2013 election results were disputed by Hun Sen's opposition, leading to demonstrations in the capital. Demonstrators were injured and killed in Phnom Penh where a reported 20,000 protesters gathered, with some clashing with riot police.[73] From a humble farming background, Hun Sen was just 33 when he took power in 1985, and is now in the company of other long ruling dictators such as Zimbabwe's Robert Mugabe and Kazakhstan's Nursultan Nazarbayev'.[74]

National politics in Cambodia take place within the framework of the nation's constitution of 1993. The government is a constitutional monarchy operated as a parliamentary representative democracy. The Prime Minister of Cambodia, an office held by Hun Sen since 1985, is the head of government, while the King of Cambodia (currently Norodom Sihamoni) is the head of state. The prime minister is appointed by the king, on the advice and with the approval of the National Assembly. The prime minister and the ministerial appointees exercise executive power.

Legislative powers are shared by the executive and the bicameral Parliament of Cambodia, which consists of a lower house, the National Assembly (រដ្ឋសភាកម្ពុជា, rotsaphea) and an upper house, the Senate (ព្រឹទ្ធសភានៃព្រះរាជាណាចក្រកម្ពុជា, preutsaphea). Members of the 123-seat Assembly are elected through a system of proportional representation and serve for a maximum term of five years. The Senate has 61 seats, two of which are appointed by the king and two others by the National Assembly, and the rest elected by the commune councillors from 24 provinces of Cambodia. Senators serve six-year terms.[75]

On 14 October 2004, King Norodom Sihamoni was selected by a special nine-member throne council, part of a selection process that was quickly put in place after the abdication of King Norodom Sihanouk a week prior. Sihamoni's selection was endorsed by Prime Minister Hun Sen and National Assembly Speaker Prince Norodom Ranariddh (the king's half brother and current chief advisor), both members of the throne council. He was enthroned in Phnom Penh on 29 October 2004.
